BLOB = "https://github.com/perrelet/teapots/blob/main/mythos/entries/"


def esc(s):
    return (s.replace("&", "&amp;").replace("<", "&lt;")
             .replace(">", "&gt;").replace('"', "&quot;"))


def build_thumbs():
    try:
        from PIL import Image
    except ImportError:
        print("Pillow missing; skipping thumbnails")
        return 0
    tdir = ROOT / "assets" / "thumbs"
    tdir.mkdir(parents=True, exist_ok=True)
    n = 0
    for e in entries:
        img = e.get("image", "")
        if not img or img.endswith(".svg"):
            continue
        dst = tdir / pathlib.Path(img).name
        try:
            im = Image.open(ROOT / img)
            im.thumbnail((400, 400))
            if dst.suffix.lower() in (".jpg", ".jpeg") and im.mode in ("RGBA", "P", "LA"):
                im = im.convert("RGB")
            im.save(dst)
            n += 1
        except Exception as ex:  # noqa
            print("thumb fail", img, ex)
    return n


def _thumb_src(e):
    img = e.get("image", "")
    if not img:
        return ""
    if img.endswith(".svg"):
        return "../" + img
    return "../assets/thumbs/" + pathlib.Path(img).name


